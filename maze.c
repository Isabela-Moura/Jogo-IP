{ // 1 coluna e 2 linhas

    int *ponteiro = &maze[linha1][coluna];  // Ponteiro apontando para o elemento da matriz
    int *ponteiro2 = &maze[linha2][coluna]; // Ponteiro apontando para o elemento da matriz
    if (*ponteiro == 1)
    {
        // Muda o valor do elemento para 0
        *ponteiro = 0;
        *ponteiro2 = 0;
    }
}
